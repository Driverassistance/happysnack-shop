        analysis = "\nüë• –ê–ù–ê–õ–ò–ó –ü–û–ö–£–ü–ê–¢–ï–õ–ï–ô –¢–í–û–ï–ì–û –ú–ê–ì–ê–ó–ò–ù–ê:\n\n"
        
