            analysis += "   üí° –†–µ–∫–æ–º–µ–Ω–¥—É—é: –≠–Ω–µ—Ä–≥–µ—Ç–∏–∫–∏ (Red Bull), –ø—Ä–µ–º–∏—É–º —á–∏–ø—Å—ã (Pringles)\n\n"
        
