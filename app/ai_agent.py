            analysis += "   –û—Å–Ω–æ–≤–Ω–∞—è –∞—É–¥–∏—Ç–æ—Ä–∏—è: –ú–æ–ª–æ–¥–µ–∂—å 18-30 –ª–µ—Ç\n"
