            # 1. –î–∞–≤–Ω–æ –Ω–µ –∑–∞–∫–∞–∑—ã–≤–∞–ª (>14 –¥–Ω–µ–π)
            if days_since_last > 14:
                should_contact = True
                reason = f"–ù–µ –∑–∞–∫–∞–∑—ã–≤–∞–ª {days_since_last} –¥–Ω–µ–π"
            
            # 2. –ú–Ω–æ–≥–æ –±–æ–Ω—É—Å–æ–≤ (>1000‚Ç∏)
